package java_term_project;

public class ID_PassWord {
	private String ID;
	private String PW;
	private String PW_Start;
	

	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String PW) {
		this.PW = PW;
	}
	public String getPW_Start() {
		return PW_Start;
	}
	public void setPW_Start(String PW_Start) {
		this.PW_Start = PW_Start;
	}

}
